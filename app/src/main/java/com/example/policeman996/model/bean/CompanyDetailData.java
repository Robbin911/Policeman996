package com.example.policeman996.model.bean;

public class CompanyDetailData {

  private int totalRate;
  private int startWorkTimeWeek;
  private int offWorkTimeWeek;
  private int startWorkTimeMonth;
  private int offWorkTimeMonth;
  private int workOvertime;
  private int welfare;

  public int getTotalRate() {
    return totalRate;
  }

  public void setTotalRate(int totalRate) {
    this.totalRate = totalRate;
  }

  public int getStartWorkTimeWeek() {
    return startWorkTimeWeek;
  }

  public void setStartWorkTimeWeek(int startWorkTimeWeek) {
    this.startWorkTimeWeek = startWorkTimeWeek;
  }

  public int getOffWorkTimeWeek() {
    return offWorkTimeWeek;
  }

  public void setOffWorkTimeWeek(int offWorkTimeWeek) {
    this.offWorkTimeWeek = offWorkTimeWeek;
  }

  public int getStartWorkTimeMonth() {
    return startWorkTimeMonth;
  }

  public void setStartWorkTimeMonth(int startWorkTimeMonth) {
    this.startWorkTimeMonth = startWorkTimeMonth;
  }

  public int getOffWorkTimeMonth() {
    return offWorkTimeMonth;
  }

  public void setOffWorkTimeMonth(int offWorkTimeMonth) {
    this.offWorkTimeMonth = offWorkTimeMonth;
  }

  public int getWorkOvertime() {
    return workOvertime;
  }

  public void setWorkOvertime(int workOvertime) {
    this.workOvertime = workOvertime;
  }

  public int getWelfare() {
    return welfare;
  }

  public void setWelfare(int welfare) {
    this.welfare = welfare;
  }
}
